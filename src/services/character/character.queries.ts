/**
 * GraphQL Queries para Character
 * Todas las queries relacionadas con personajes están centralizadas aquí
 */

export const GET_CHARACTERS_QUERY = `
  query GetCharacters($page: Int, $filter: FilterCharacter) {
    characters(page: $page, filter: $filter) {
      info {
        count
        pages
        next
        prev
      }
      results {
        id
        name
        status
        species
        type
        gender
        origin {
          name
        }
        location {
          name
        }
        image
        episode {
          id
          name
        }
        created
      }
    }
  }
`;

